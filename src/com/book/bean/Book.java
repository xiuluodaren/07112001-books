package com.book.bean;

public class Book {

	private int id;
	private String gname;
	private String gurl;
	private int sjid;
	private int btid;
	private int states;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public String getGurl() {
		return gurl;
	}
	public void setGurl(String gurl) {
		this.gurl = gurl;
	}
	public int getSjid() {
		return sjid;
	}
	public void setSjid(int sjid) {
		this.sjid = sjid;
	}
	public int getBtid() {
		return btid;
	}
	public void setBtid(int btid) {
		this.btid = btid;
	}
	public int getStates() {
		return states;
	}
	public void setStates(int states) {
		this.states = states;
	}
	
}
