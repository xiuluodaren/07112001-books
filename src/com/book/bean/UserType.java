package com.book.bean;

public class UserType {

	private int id;
	private String names;
	private int kj;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNames() {
		return names;
	}
	public void setNames(String names) {
		this.names = names;
	}
	public int getKj() {
		return kj;
	}
	public void setKj(int kj) {
		this.kj = kj;
	}
	
}
